    <h1>Pertama di Jatim, Wapres Ma’ruf Amin Resmikan Zona KHAS ITS</h1>
    <img src="https://www.its.ac.id/news/wp-content/uploads/sites/2/2023/08/Wakil-Presiden-RI-Prof-Dr-KH-Maruf-Amin-saat-menyampaikan-arahan-dalam-kegiatan-Sarasehan-Ekonomi-dan-Keuangan-Syariah-sebelum-meresmikan-Zona-KHAS-ITS.jpeg" width="60%">
    <div class="deskripsiimg">
        Wakil Presiden RI Prof Dr KH Ma’ruf Amin saat menyampaikan arahan dalam kegiatan Sarasehan Ekonomi dan Keuangan Syariah sebelum meresmikan Zona KHAS ITS di Hotel Sheraton Surabaya
    </div>
    <br>
    <div class="pembuka">
        Surabaya, ITS News – Dalam rangka menghadirkan lingkungan kuliner yang berkualitas dan responsif terhadap
        tuntutan makanan yang halal, aman, dan sehat, Institut Teknologi Sepuluh Nopember (ITS) mengembangkan Zona
        Kuliner Halal, Aman dan Sehat (Zona KHAS). Menjadi yang pertama di Jawa Timur untuk kalangan perguruan tinggi,
        zona ini diresmikan secara langsung oleh Wakil Presiden Republik Indonesia (RI), melalui kegiatan Sarasehan
        Ekonomi dan Keuangan Syariah di Hotel Sheraton Surabaya, Rabu (30/8).
    </div>
    <br>
    <div class="isiberita">
        Peresmian ini juga didampingi oleh Wakil Gubernur Jawa Timur Emil Elestianto Dardak, Direktur Bisnis dan
        Kewirausahaan Syariah Komite Nasional Ekonomi dan Keuangan Syariah (KNEKS) Ir Putu Rahwidhiyasa, Rektor ITS Prof
        Dr Ir Mochamad Ashari MEng IPU AEng dan Pengasuh Pondok Pesantren Amanatul Ummah Prof Dr KH Asep Saifuddin
        Chalim MA. Seremoni peresmian juga dihadiri oleh para anggota Forum Koordinasi Pimpinan Daerah (Forkopimda) Jawa
        Timur, anggota Komite Daerah Ekonomi dan Keuangan Syariah Jawa Timur serta para penggiat ekonomi dan keuangan
        syariah di Provinsi Jawa Timur.
    </div>
